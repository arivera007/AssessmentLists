def all_odd(numbers):
    """Return a list of only the odd numbers in the input list.

    For example::

        >>> all_odd([1, 2, 7, -5])
        [1, 7, -5]

        >>> all_odd([2, -6, 8])
        []
    """

    #return filter(lambda number: number % 2 != 0, numbers)          # Filtered out what has a remainder different from 0 when divided by 2.
    return [number for number in numbers if number % 2 != 0]         # or generating a comprehention list 


